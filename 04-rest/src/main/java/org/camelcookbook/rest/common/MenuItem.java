package org.camelcookbook.rest.common;

import javax.validation.constraints.NotNull;

public class MenuItem {
    private int id;
    private String name;
    private int cost;
    private String description;

    public MenuItem() {
    }

    public MenuItem(@NotNull MenuItem item) {
        this.id = item.id;
        this.name = item.name;
        this.cost = item.cost;
        this.description = item.description;
    }

    public MenuItem(int id, String name, int cost, String description) {
        this.id = id;
        this.name = name;
        this.cost = cost;
        this.description = description;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCost() {
        return cost;
    }

    public void setCost(int cost) {
        this.cost = cost;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        MenuItem menuItem = (MenuItem) o;

        if (id != menuItem.id) return false;
        if (cost != menuItem.cost) return false;
        if (name != null ? !name.equals(menuItem.name) : menuItem.name != null) return false;
        return description != null ? description.equals(menuItem.description) : menuItem.description == null;
    }

    @Override
    public int hashCode() {
        int result = id;
        result = 31 * result + (name != null ? name.hashCode() : 0);
        result = 31 * result + cost;
        result = 31 * result + (description != null ? description.hashCode() : 0);
        return result;
    }

    @Override
    public String toString() {
        return "MenuItem{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", cost=" + cost +
                ", description='" + description + '\'' +
                '}';
    }
}
